def plus1(number: int) -> int:
    """This function will add 1 to a number and return it

    Args:
        number (int): A number

    Returns:
        int: The original number plus 1
    """
    return number + 1


def plus3(number: int) -> int:
    """This function will add 3 to a number and return it

    Args:
        number (int): A number

    Returns:
        int: The original number plus 3
    """
    return number + 3
